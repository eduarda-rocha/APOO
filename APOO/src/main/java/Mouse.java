public class Mouse extends Produtos {
	
	private String dpiMou;
	private String fioMou;
	private String corMou;
	
	public String getDpiMou() {
		return this.dpiMou;
	}
	public void setDpiMou(String dpiMou) {
		this.dpiMou = dpiMou;
	}
	public String getFioMou() {
		return this.fioMou;
	}
	public void setFioMou(String fioMou) {
		this.fioMou = fioMou;
	}
	public String getCorMou() {
		return this.corMou;
	}
	public void setCorMou(String corMou) {
		this.corMou = corMou;
	}
	
}
