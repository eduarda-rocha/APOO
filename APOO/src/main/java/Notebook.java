public class Notebook extends Produtos  {
	
	private String soNot;
	private String cpuNot;
	private String ramNot;
	private String hddNot;
	private String ssdNot;
	private String gpuNot;
	
	public String getSoNot() {
		return this.soNot;
	}
	public void setSoNot(String soNot) {
		this.soNot = soNot;
	}
	public String getCpuNot() {
		return this.cpuNot;
	}
	public void setCpuNot(String cpuNot) {
		this.cpuNot = cpuNot;
	}
	public String getRamNot() {
		return this.ramNot;
	}
	public void setRamNot(String ramNot) {
		this.ramNot = ramNot;
	}
	public String getHddNot() {
		return this.hddNot;
	}
	public void setHddNot(String hddNot) {
		this.hddNot = hddNot;
	}
	public String getSsdNot() {
		return this.ssdNot;
	}
	public void setSsdNot(String ssdNot) {
		this.ssdNot = ssdNot;
	}
	public String getGpuNot() {
		return this.gpuNot;
	}
	public void setGpuNot(String gpuNot) {
		this.gpuNot = gpuNot;
	}
	
	
}
